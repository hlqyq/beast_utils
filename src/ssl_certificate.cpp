template<typename FunctorCBType>
const std::string invoke_python_string_cb(FunctorCBType functor_cb, std::size_t init_size = 1024) {
    for (std::size_t buffer_size = init_size, valid_size = init_size * 2; valid_size > buffer_size; buffer_size *= 2) {
        std::string buffer(buffer_size, '\0');
        valid_size = functor_cb(&buffer[0], static_cast<unsigned int>(buffer.size()));
        if (valid_size < buffer_size)
            return buffer.substr(0, valid_size);
    }
    return "";
}

